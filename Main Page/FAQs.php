		<h3 id="header">Why Student Attendance Is Important ?</h3>
		<p>Attendance management system is essential for any organization such as education field by keeping track the performance of students
		such as how regular student attend for class session to have an accurate graphical depiction of everything related to presence and absence.
		Consider the impact to the success of any academic institution by using disorganized attendance management system, education fields are unable
		to achieve its main purposes as many of research had been conducted and reveal that poor attendance is significantly jeopardize the quality of education.
		By computerized attendance management system, attendance control has conventionally been approached by using real-time. </p><br>
		
		<h3 id="header">Why Should I Choose To Use Attendance Management System ?</h3>
		<p>Paper-based attendance system is time-consuming as the attendance sheets are slowly passed from one student to another. Worse, untidy and inconsistent 
		handwritting makes the attendance taking become inefficient and certain students are not able to register their attendances before the end of a short lecture.
		Besides nowadays students are attending online classes resulting paper-based attendance difficulty to proceed with.</p>
		<p>Bias and Fabrication of Attendance Data will also likely occur among students especially for paper-based attendance system when student take attendance in a 
		name list or class representative takes attendance for students. Class representative or any presence student will possibly fabricate the data by taking attendance 
		for absence student.</p>
		<p>Lecturer and admin are difficult to real-time tracking for student's attendance. Worse, the quantity of attendance is increasing day by day and will typically
		need to be stored close to hand.</p>	
		<p>Difficult for the lecturers and admins to access the data and update the data after making modifications through file-based approach. manual documents can be 
		easily damaged, lost, misplaced or stolen.</p><br>
		
		<h3 id="header">How Do I Use Attendance Management System ?</h3>
		<p>Students will login to their system account individually and takes attendance individually or lecturer can take attendance for students. Each module has been 
		provided with its own time limit and students can only take attendance individually within the time limit. If student fails to take attendance after the time limit
		or due to absence purposes, student should provide valid reason by submitting External Circumstances (EC) to the provided section while only lecturer can review
		student's EC and made modification for student attendance.</p>
		<p>For lecturer and administrator, the new system could update the data automatically after any modification is done. The data that stores in the database will not 
		lost easily and it could be recovered easily after losing it. It also contains accurate real-time records of students, minimize the inevitable, costly errors with 
		manual data entry.</p><br>
		
